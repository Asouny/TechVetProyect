    private void btnAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar1ActionPerformed
       Date fechaSeleccionada = jDateChooser1.getDate();

        date = "";
        met = "";
        motiv = "";
        cant = 0;
        name = "";
        id = 0;

        try {

            cant = Integer.parseInt(txtMonto.getText());
        }//catch nombre
        catch (NumberFormatException e) {
            showMessageDialog(this, "Solo nÃºmeros enteros");
            txtMonto.setText("");
            txtMonto.requestFocus();
        }

        String partes[] = jcbClientes.getSelectedItem().toString().split(" - ");

        name = partes[1];

        idCliente = Integer.parseInt(partes[0]);

        date = fechaSeleccionada + "";

        met = jcbMetodo.getSelectedItem().toString();
        if (jcbMetodo.getSelectedIndex() == 0) { //index cuando sea entero
            showMessageDialog(this, "Selecciona un mÃ©todo de pago");
            jcbMetodo.requestFocus();
        }
        motiv = jcbMotivo.getSelectedItem().toString();
        if (jcbMotivo.getSelectedIndex() == 0) { //index cuando sea entero
            showMessageDialog(this, "Selecciona un motivo de pago");
            jcbMotivo.requestFocus();
        } else {
            showMessageDialog(this, "Pago registrado con exito");

            

            P[p++] = new Pago(date, met, motiv, cant, idCliente);

            
            controlador.guardar("Pagos", P);
            txtMonto.setText("");
            txtID.setText("");
            jcbMetodo.setSelectedIndex(0);
            jcbMotivo.setSelectedIndex(0);
            jcbClientes.setSelectedIndex(0);
            fechaSeleccionada = null;

        }
    }//GEN-LAST:event_btnAgregar1ActionPerformed

    private void leerPagos(){
        P = (Pago[]) controlador.leer("Pagos", Pago.class);
        Object R[] = new Object[5];
        for(int i = 0; i< P.length; i++){
            if(P[i] == null){
                return;
            }
            id = P[i].getClienteID();
            p++;
        }
    }
    
    private int buscaPago(int id, Pago P[]){
        for (int i = 0; i < p; i++) {
            if(id == P[i].getClienteID()){
                return i;
            }
            
        }
        return -1;
    }
    
    private void leerClientes() {

        C = (Cliente[]) controlador.leer("Clientes", Cliente.class);

        // Agregar los clientes al combo box
        for (Cliente cliente : C) {
            if (cliente == null) {
                return; //si el renglon que quiere agregar estÃ¡ vacÃ­o, no lo agregarÃ¡ y no marcarÃ¡ error
            }
            String item = cliente.getId() + " - " + cliente.getNombre();
            jcbClientes.addItem(item);
        }
        }
