    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int tratamientoEDITPOS = buscaTratamiento(userSelectedID, T);

        if (showConfirmDialog(this, "Se borrará el renglón, ¿proceder?") == 0) {
            m.removeRow(tratamientoEDITPOS); // pos, pues es la posicion de la que se habla, se borra en la tabla
            eliminarTratamiento(tratamientoEDITPOS);
            guardarArray(); //para guardarlo en el archivo
            //txtID.setText("");

            btnEditar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void guardarArray() {

        try {
            //se abre el flujo y se manda el metodo para guardar
            FileOutputStream flujoBytes = new FileOutputStream("Tratamientos.obj");
            fcs = new ObjectOutputStream(flujoBytes);
            fcs.writeObject(T);
            fcs.flush();
        } catch (IOException ex) {
            showMessageDialog(this, "Error");
        }

    }

    private void leerTratamientos() {
        try {
            FileInputStream flujoBytes = new FileInputStream("Tratamientos.obj"); //flujo de Bytes
            fce = new ObjectInputStream(flujoBytes); //flujo de objetos
            T = (Tratamiento[]) fce.readObject();
            Object R[] = new Object[4];
            m.setRowCount(0);

            //ciclo que si se cumple, manda a agregar a la tabla para cuando inicie el programa de nuevo
            for (int i = 0; i < T.length; i++) {
                if (T[i] == null) {
                    return; //si el renglon que quiere agregar está vacío, no lo agregará y no marcará error
                }

                R[0] = T[i].getId();
                R[1] = T[i].getMedicinas();
                R[2] = T[i].getFecha();
                R[3] = T[i].getDuracion();
               
                id = T[i].getId();
                m.addRow(R);
                t++;
            }

        } catch (FileNotFoundException ex) {
            showMessageDialog(this, "Error el archivo no se encontro");
        } catch (IOException ex) {
            showMessageDialog(this, "Error");
        } catch (ClassNotFoundException ex) {
            showMessageDialog(this, "Error");
        }
    }

    public int buscaTratamiento(int ca, Tratamiento A[]) {
        for (int i = 0; i < A.length; i++) {
            if(A[i]== null){
                return -1;
            }
            if (ca == T[i].getId()) {
                if (T[i] == null) {
                    return -1; //si el renglon que quiere agregar está vacío, no lo agregará y no marcará error
                }
                return i; //Return el arreglo en esa posición
            }//Para que compare id (parámetro) con id del arreglo
        }
        return -1; //Por si no los encontró

    }

    private int obtenerIdTratamientoSeleccionado() {
        // Obtener el índice de la fila seleccionada en la tabla
        int rowIndex = tblTratamientos.getSelectedRow();

        // Verificar si hay una fila seleccionada
        if (rowIndex != -1) {
            // Obtener el valor del ID del cliente en la columna 0 (primera columna)
            int idTratamiento = (int) tblTratamientos.getValueAt(rowIndex, 0);

            btnEditar.setEnabled(true);
            btnEliminar.setEnabled(true);
            // retornar el ID del cliente en un mensaje
            return idTratamiento;
        } else {
            return -1;
        }
    }

    public void eliminarTratamiento(int posicion) {

        // Copiar los elementos antes de la posición
        System.arraycopy(T, 0, T, 0, posicion);

        // Copiar los elementos después de la posición
        System.arraycopy(T, posicion + 1, T, posicion, T.length - posicion - 1);

        // Asignar null al último elemento
        T[T.length - 1] = null;

    }

    private void llenaTabla() {
        for (int i = 0; i < t; i++) {
            //if
            m.setValueAt(T[i].getId(), i, 0);
            m.setValueAt(T[i].getMedicinas(), i, 1);
            m.setValueAt(T[i].getFecha(), i, 2);
            m.setValueAt(T[i].getDuracion(), i, 3);
            

        }

    }

    
