    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtSearch.getText().equals("")) {
            leerTratamientos();
            return;
        } else {
            int id = Integer.parseInt(txtSearch.getText());

            int tratamientoPOS = buscaTratamiento(id, T);

            if (tratamientoPOS == -1) {
                showMessageDialog(this, "Registro no encontrado");

            } else {
                Tratamiento tratamientoEncontrado = T[tratamientoPOS];
                m.setRowCount(0);

                Object R[] = new Object[4];
                //ciclo que si se cumple, manda a agregar a la tabla para cuando inicie el programa de nuevo

                R[0] = tratamientoEncontrado.getId();
                R[1] = tratamientoEncontrado.getMedicinas();
                R[2] = tratamientoEncontrado.getFecha();
                R[3] = tratamientoEncontrado.getDuracion();
                
                id = tratamientoEncontrado.getId();
                m.addRow(R);
                t++;
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

